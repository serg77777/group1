
public class NumericTypes {
	public static void main(String[] args) {
		
		byte b = Byte.MIN_VALUE; // 8 bit, 1 byte
		b = Byte.MAX_VALUE;
		
		short s = Short.MIN_VALUE; // 16 bit, 2 bytes
		s = Short.MIN_VALUE;
		
		char c = 'A';	// 16 bit, 2 bytes
		c = 65;
		c = Character.MIN_VALUE;
		c = Character.MAX_VALUE;
		
		int i = Integer.MIN_VALUE; // 32 bit, 4 bytes
		i = Integer.MAX_VALUE;
		
		long l = Long.MIN_VALUE; // 64 bit, 8 bytes
		l = Long.MAX_VALUE;
		
		float f = Float.MIN_VALUE; // 32 bit, 4 bytes
		f = Float.MAX_VALUE;
		
		double d = Double.MIN_VALUE; // 128 bit, 16 bytes
		d = Double.MAX_VALUE;
		
		boolean bool = true;
		bool = false;
		
	}
}

package lesson140225;

public class NumericTypes {
	public static void main(String[] args) {
		byte b = Byte.MAX_VALUE;
		b = Byte.MIN_VALUE;
		
		short sh = Short.MAX_VALUE;
		sh = Short.MIN_VALUE;
		
		int i = Integer.MAX_VALUE;
		i = Integer.MIN_VALUE;
		
		char c = 'A';
		c = Character.MAX_VALUE;
		c = Character.MIN_VALUE;
	}
}

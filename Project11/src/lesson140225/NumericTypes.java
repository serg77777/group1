package lesson140225;

public class NumericTypes {
	public static void main(String[] args) {
		
		byte b = Byte.MIN_VALUE; //8 bit
		b = Byte.MAX_VALUE;
		
		short s = Short.MIN_VALUE; //16 bit
		s = Short.MAX_VALUE;
		
		char c = 'A';
		c = 65; //-65
		c = Character.MIN_VALUE; //16 bit
		c = Character.MAX_VALUE;
	
		int i = Integer.MIN_VALUE; //32 bit
		i = Integer.MAX_VALUE;
		
		long l = Long.MIN_VALUE; //64 bit
		l = Long.MAX_VALUE;
		
		float f = Float.MIN_VALUE; //32 bit
		f = Float.MAX_VALUE;
		
		double d = Double.MIN_VALUE; //64 bit
		d = Double.MAX_VALUE;
		
		boolean bool = true;
		bool = false;
		
	}
}
